def write_chapter2(doc):
    add_heading_styled(doc, '第二章  技术需求分析', 1)
    
    # 2.1
    add_heading_styled(doc, '2.1  功能性需求分析', 2)
    
    add_heading_styled(doc, '2.1.1  数据资产管理需求', 3)
    
    add_para(doc, '数据资产管理是能源可信数据空间的基础功能模块，负责实现数据的全生命周期管理。根据《数据管理能力成熟度模型》（GB/T 36073-2018，即DCMM国家标准）的定义，数据资产管理涵盖数据战略、数据治理、数据架构、数据质量、数据安全、数据标准、数据应用、数据生存周期等八个能力域。本章节结合DCMM标准和能源行业特点，从以下维度分解功能性需求：')
    
    add_para(doc, '（1）数据源管理。系统应支持多类型数据源的接入和管理，包括关系型数据库（PostgreSQL、MySQL、Oracle）、时序数据库（InfluxDB、TDengine）、文件存储（MinIO/S3对象存储）、消息队列（MQTT、Kafka）、API接口（REST、GraphQL）等。数据源连接应支持SSL/TLS加密通信，连接凭证应加密存储。系统应提供数据源连通性测试和元数据自动发现功能，减少人工配置工作量。')
    
    add_para(doc, '（2）数据资产管理。系统应建立统一的数据资产目录，支持按业务域、数据类型、安全等级、组织部门等多维度分类管理。数据资产应包括结构化数据（数据库表、视图）、半结构化数据（JSON、XML、Parquet）、非结构化数据（文档、图像、视频）、流式数据（实时传感器数据、消息队列数据）等。每项数据资产应具有唯一的资产标识符（统一资源标识符URI），并关联元数据信息。')
    
    add_para(doc, '（3）元数据管理。系统应实现技术元数据、业务元数据、操作元数据的统一管理。技术元数据包括数据库模式、表结构、字段定义、索引信息、分区信息等；业务元数据包括数据业务含义、数据责任部门、数据分类分级标签、数据血缘关系等；操作元数据包括数据更新时间、数据访问频率、数据质量评分等。元数据管理应支持自动采集和手动标注两种方式。')
    
    add_para(doc, '（4）数据目录与检索。系统应提供数据资产的全景目录视图，支持关键词搜索、标签过滤、分类浏览等多种检索方式。搜索功能应支持模糊匹配、精确匹配、正则表达式等模式。搜索结果应按相关度排序，并展示数据资产的关键元数据信息（名称、描述、分类、更新时间、数据量、质量评分等）。系统应支持基于角色的数据目录访问控制，不同权限用户可见不同范围的数据资产。')
    
    add_para(doc, '（5）数据血缘管理。系统应自动追踪数据从源系统到目标系统的流转路径，构建端到端的数据血缘图谱。数据血缘应精确到字段级别，并记录每一次数据转换、清洗、聚合操作。血缘信息应支持向前追溯（分析某个数据资产被哪些下游应用消费）和向后追溯（分析某个数据资产来源于哪些上游系统）。数据血缘的可视化展示应支持交互式探索。')
    
    add_para(doc, '（6）数据质量管理。系统应建立数据质量的评估体系和监控机制。数据质量评估维度包括完整性（Completeness）、准确性（Accuracy）、一致性（Consistency）、时效性（Timeliness）、唯一性（Uniqueness）、有效性（Validity）六个维度（合称"CAUTUV"模型）。系统应支持数据质量规则的灵活配置和自动执行，对于质量不达标的数据应触发告警和修复流程。')
    
    add_heading_styled(doc, '2.1.2  可信数据共享需求', 3)
    
    add_para(doc, '可信数据共享是能源可信数据空间的核心功能，旨在解决能源行业数据"不敢共享、不愿共享、不能共享"的困局。核心需求包括：')
    
    add_para(doc, '（1）数据使用控制。系统应实现基于策略的数据使用控制（Usage Control），超越传统访问控制（Access Control）的范畴。使用控制策略应涵盖数据使用目的（Purpose）、使用范围（Scope）、使用期限（Duration）、使用次数（Frequency）、使用环境（Environment）等维度。例如，某电力负荷数据的使用策略可以定义为"仅允许在安全等级为L3的计算环境中用于负荷预测模型训练，使用期限不超过30天，不得复制和转发"。使用控制策略应在数据使用全过程中持续执行，而非仅在访问时执行一次。')
    
    add_para(doc, '（2）隐私计算能力。系统应提供多种隐私计算技术路线，以适应不同安全等级和性能要求的应用场景：')
    
    add_table = doc.add_table(rows=6, cols=5)
    add_table.style = 'Table Grid'
    headers = ['技术路线', '安全模型', '计算精度', '性能开销', '典型应用场景']
    rows_data = [
        ['联邦学习（FL）', '数据不出域，模型参数交换', '近似精度（与中心化训练相当）', '中（通信开销为主）', '负荷预测模型联合训练\n设备故障诊断模型训练'],
        ['可信执行环境（TEE）', '硬件级 enclave 隔离', '精确计算', '低（接近明文计算）', '数据质量校验\n合规审查计算'],
        ['安全多方计算（MPC）', '密码学安全，信息论安全', '精确计算', '高（通信+计算开销大）', '电力市场联合出清\n隐私集合求交'],
        ['同态加密（HE）', '密码学安全，可证安全', '精确计算/近似', '极高（密文膨胀100-1000x）', '密态数据聚合\n隐私保护统计分析'],
        ['差分隐私（DP）', '统计安全，ε-差分隐私', '有损（加噪）', '低', '统计报告发布\n敏感数据脱敏'],
    ]
    for i, h in enumerate(headers):
        cell = add_table.rows[0].cells[i]
        cell.text = ''
        p = cell.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(h)
        run.bold = True; run.font.size = Pt(8); run.font.color.rgb = RGBColor(255,255,255)
        set_cell_shading(cell, "1A3C6D")
    for r, row in enumerate(rows_data):
        for c, val in enumerate(row):
            cell = add_table.rows[r+1].cells[c]
            cell.text = ''
            p = cell.paragraphs[0]
            run = p.add_run(val)
            run.font.size = Pt(7.5)
            if r % 2 == 1: set_cell_shading(cell, "F0F4FA")
    doc.add_paragraph('')
    
    add_para(doc, '（3）数据授权与审批。系统应建立完善的数据授权管理机制，支持数据提供方对其数据进行细粒度的授权控制。授权模式应包括：基于角色的授权（Role-Based Authorization，将数据访问权限授予特定角色）、基于属性的授权（Attribute-Based Authorization，根据请求方的属性动态决策）、基于策略的授权（Policy-Based Authorization，通过策略语言描述授权规则）。授权审批流程应支持多级审批、会签审批、自动审批等多种模式，审批记录应完整留痕并存证到区块链。')
    
    add_para(doc, '（4）数据连接器。系统应实现符合IDSA参考架构标准的数据连接器，作为数据空间的基础入口。数据连接器应支持数据提供者和数据消费者两种角色，提供数据传输通道加密、使用控制策略执行、使用日志记录等核心功能。连接器应支持多种数据传输协议，包括HTTP/REST、gRPC、MQTT、SFTP等物理传输方式，并在其上层实现数据空间的应用层协议。')
    
    add_heading_styled(doc, '2.1.3  区块链存证需求', 3)
    
    add_para(doc, '区块链作为可信数据空间的信任基础设施，承担数据存证、交易记录、合约执行等核心功能：')
    
    add_para(doc, '（1）数据存证。系统应将数据资产的关键操作记录（注册、发布、授权、访问、下载、更新、销毁）以区块链交易的形式进行存证。存证内容应包括操作类型、操作时间、操作主体、操作对象、操作结果等关键信息，以及原始数据的哈希值（SHA-256或SM3）。存证数据应不可篡改且可公开验证。系统应支持批量存证以降低区块链交互成本。')
    
    add_para(doc, '（2）智能合约。系统应部署数据交易智能合约，实现数据交易的自动化执行。智能合约应涵盖以下交易类型：数据产品上架/下架、数据购买/订阅、数据使用权转移、收益自动分配、违约自动惩罚等。合约代码应在部署前通过安全审计，避免重入攻击、整数溢出等常见智能合约漏洞。合约应支持可升级性，以适应业务规则的变化。')
    
    add_para(doc, '（3）数据资产NFT化。系统应支持将数据资产铸造为NFT（Non-Fungible Token，非同质化代币），赋予数据资产唯一标识和可追溯的所有权记录。数据NFT应记录数据资产的详细元数据、授权条款、定价信息和使用历史。NFT应支持转移、授权、销毁等标准操作。')
    
    add_heading_styled(doc, '2.1.4  安全认证与身份管理需求', 3)
    
    add_para(doc, '分布式数字身份（DID）和可验证凭证（VC）是能源可信数据空间中跨组织身份互认的技术基础：')
    
    add_para(doc, '（1）DID身份管理。系统应支持W3C DID Core 1.0标准，为每个参与方（组织、用户、设备）创建唯一的分布式标识符。DID文档应包含公钥信息、服务端点（Service Endpoint）、认证方法等关键属性。DID应支持多种方法（Method），包括did:key、did:web、did:ethr等。系统应实现DID的CRUD操作，并将DID文档的更新记录在区块链或分布式账本上，确保不可篡改。')
    
    add_para(doc, '（2）VC凭证管理。系统应支持W3C Verifiable Credentials Data Model 2.0标准，实现可验证凭证的签发、持有、验证和撤销。VC的类型应包括身份凭证（证明持有者身份）、资质凭证（证明持有者的角色/资质）、授权凭证（证明持有者对某资源的访问权限）、数据产品凭证（描述数据产品的属性和使用条款）。VC应支持选择性披露（Selective Disclosure），即持有者可以选择性地出示凭证中的部分属性，而不泄露全部信息。')
    
    add_para(doc, '（3）多因素认证（MFA）。系统应支持基于TOTP（Time-based One-Time Password）标准的多因素认证，增强账户安全性。应支持MFA的绑定、验证、解绑、重置等完整生命周期管理，并提供备用恢复码机制。')
    
    add_para(doc, '（4）单点登录（SSO）。系统应支持OAuth 2.0、OIDC（OpenID Connect）和SAML 2.0等标准协议的SSO集成，支持与企业的统一身份认证系统对接。')
    
    add_heading_styled(doc, '2.1.5  AI Agent智能体需求', 3)
    
    add_para(doc, 'AI Agent是能源可信数据空间中的智能助手，提供自然语言交互能力：')
    
    add_para(doc, '（1）自然语言查询。用户应能通过自然语言描述其数据需求，AI Agent自动将自然语言转化为查询语句或API调用，返回所需的数据或分析结果。')
    add_para(doc, '（2）智能推荐。AI Agent应根据用户的历史行为和角色信息，主动推荐相关的数据产品和分析报告。')
    add_para(doc, '（3）知识库管理。系统应维护领域知识库，包括能源行业术语、数据标准、业务规则等，辅助AI Agent准确理解用户意图。')
    add_para(doc, '（4）多种Agent类型。系统应支持查询代理、交易代理、安全代理、调度代理等多种专业Agent，各自专注不同领域的智能辅助任务。')
    
    # 2.2
    add_heading_styled(doc, '2.2  非功能性需求分析', 2)
    
    add_heading_styled(doc, '2.2.1  性能需求', 3)
    
    add_para(doc, '能源可信数据空间面向大规模能源数据处理场景，性能需求如下：')
    
    add_styled_table(doc,
        ['性能指标', '目标值', '测试条件', '优先级'],
        [
            ['API响应时间（P95）', '< 500ms（简单查询）', '500并发请求', '高'],
            ['API响应时间（P95）', '< 3s（复杂分析）', '500并发请求', '高'],
            ['数据上传吞吐量', '> 100MB/s', '单节点，10GB文件', '中'],
            ['数据下载吞吐量', '> 200MB/s', '单节点，10GB文件', '中'],
            ['联邦学习模型训练', '< 24h', '10方参与，100GB数据，10轮迭代', '中'],
            ['区块链TPS', '> 100 TPS', '单链，简单交易', '中'],
            ['系统可用性', '> 99.9%（年）', '全年运行', '高'],
            ['故障恢复时间（RTO）', '< 30分钟', '主节点故障', '高'],
            ['数据恢复点（RPO）', '< 5分钟', '数据库故障', '高'],
        ],
        col_widths=[3.0, 2.5, 3.0, 1.2]
    )
    
    add_heading_styled(doc, '2.2.2  安全需求', 3)
    
    add_para(doc, '作为涉及国家关键信息基础设施的系统，能源可信数据空间的安全需求等级极高：')
    
    add_bullet(doc, '数据加密：传输中的数据必须使用TLS 1.3及以上版本加密；存储的数据必须使用AES-256-GCM或国密SM4加密；密钥管理必须符合NIST SP 800-57或GM/T相关标准。')
    add_bullet(doc, '访问控制：必须实现基于角色的访问控制（RBAC）和基于属性的访问控制（ABAC），支持最小权限原则。敏感操作必须经过多人审批。')
    add_bullet(doc, '审计日志：所有数据访问操作必须完整记录审计日志，包括操作主体、时间、对象、类型、结果。日志必须防篡改存储，保留期不少于180天。')
    add_bullet(doc, '安全认证：系统必须通过等级保护三级（等保三级）测评。隐私计算模块应通过中国信通院"隐私计算产品安全能力评测"。')
    add_bullet(doc, '渗透防护：系统必须能够防御OWASP Top 10常见Web安全漏洞，包括SQL注入、XSS、CSRF、SSRF等。')
    
    add_heading_styled(doc, '2.2.3  可扩展性与可维护性需求', 3)
    
    add_para(doc, '系统应支持水平扩展，各微服务模块应可独立扩容。计算密集型模块（如联邦学习训练任务）应支持GPU加速和分布式计算。系统应支持插件化架构，新的隐私计算算法应可通过插件方式集成，无需修改核心框架。代码覆盖率（单元测试）应不低于80%，关键路径应达到100%。系统应提供完整的API文档（OpenAPI 3.0/Swagger）和运维监控面板。')
    
    add_heading_styled(doc, '2.2.4  合规性需求', 3)
    
    add_para(doc, '系统设计和实现必须满足以下法律法规和标准的要求：')
    
    add_styled_table(doc,
        ['法规/标准', '适用范围', '关键要求', '系统实现要点'],
        [
            ['数据安全法', '中国境内数据处理活动', '数据分类分级、安全审查、风险评估', '支持敏感数据自动识别和分级保护'],
            ['个人信息保护法', '个人信息处理', '告知同意、最小必要、删除权', '数据脱敏、隐私计算、用户同意管理'],
            ['网络安全法', '网络运营者', '等级保护、安全认证、应急响应', '通过等保三级测评'],
            ['关键信息基础设施安全保护条例', 'CII运营者', '安全检测评估、供应链安全', '国产密码、自主可控技术栈'],
            ['GDPR（如涉及欧盟数据）', '欧盟境内数据处理', '数据主体权利、DPIA、跨境传输', '数据删除、数据可携带、DPIA模板'],
            ['ISO 27001:2022', '信息安全管理体系', 'ISMS、风险评估、持续改进', '建立ISMS并通过认证'],
            ['ISO 38505', '数据治理', '数据治理框架、原则、模型', '建立数据治理委员会和执行机制'],
        ],
        col_widths=[2.5, 2.5, 3.0, 3.5]
    )
    
    # 2.3
    add_heading_styled(doc, '2.3  系统边界与接口需求', 2)
    
    add_para(doc, '能源可信数据空间并非孤立系统，而是能源企业信息化生态中的重要一环。系统需要与以下外部系统进行数据交互和业务协同：')
    
    add_styled_table(doc,
        ['外部系统', '接口类型', '数据流向', '协议/标准', '集成优先级'],
        [
            ['电网调度自动化系统', '实时数据', '单向（读）', 'IEC 60870-5-104', '高'],
            ['用电信息采集系统', '批量/实时', '单向（读）', 'IEC 61968 CIM', '高'],
            ['电力交易中心系统', '批量同步', '双向', 'REST API/文件交换', '高'],
            ['企业ERP系统', '批量同步', '双向', 'REST API/SAP RFC', '中'],
            ['设备状态监测系统', '实时流式', '单向（读）', 'MQTT/Kafka', '中'],
            ['气象数据服务平台', '批量同步', '单向（读）', 'REST API', '中'],
            ['统一身份认证平台', '实时同步', '双向', 'OIDC/SAML 2.0/OAuth 2.0', '高'],
            ['区块链网络节点', '实时', '双向', 'JSON-RPC/WebSocket', '高'],
        ],
        col_widths=[2.5, 1.5, 1.5, 2.5, 1.5]
    )
    
    doc.add_page_break()
    print("第二章完成")

